package br.edu.fescfafic.model;

/**
 *
 * @author COMPUTER
 */
public class Moto {
    
    private String marca;
    private String cor;
    private String id;

    public Moto() {
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    } 

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}


