    private void grayscaleMediaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grayscaleMediaActionPerformed
        for(int x = 0; x < img.getWidth(); x++){
            for(int y = 0; y < img.getHeight(); y++){
                int red = new Color(img.getRGB(x, y)).getRed();
                int green = new Color(img.getRGB(x, y)).getGreen();
                int blue = new Color(img.getRGB(x, y)).getBlue();
                int media = (red + green + blue) / 3;
                Color escalaMedia = new Color(media, media, media);
                img.setRGB(x, y, escalaMedia.getRGB());
            }
        }
        jLabel1.setIcon(new ImageIcon(img));
    }//GEN-LAST:event_grayscaleMediaActionPerformed

