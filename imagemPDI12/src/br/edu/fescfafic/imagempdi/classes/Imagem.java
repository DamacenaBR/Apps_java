    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        JFileChooser salvar = new JFileChooser();
        salvar.setFileFilter(new FileNameExtensionFilter(".jpg", "jpg"));
        salvar.setAcceptAllFileFilterUsed(false);
        int res = salvar.showSaveDialog(null);
        if(res == JFileChooser.APPROVE_OPTION){
            try {
                String caminho = String.valueOf(salvar.getSelectedFile());
                ImageIO.write(img, "jpg", new File(caminho + salvar.getFileFilter().getDescription()));
            } catch (IOException ex) {
                Logger.getLogger(Imagem.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(null, "Foto salva com sucesso!");
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private String escolherImagem(){
        JFileChooser file = new JFileChooser();
        file.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int i = file.showOpenDialog(null);
        if(i == 0){
            File f = file.getSelectedFile();
            return f.getPath();
        }
        else if(i == 1){
            return null;
        }
        return null;
    }
    
    private BufferedImage abrirImagem(){
        try {
            img = ImageIO.read(new File(escolherImagem()));
            return img;
        } catch (IOException ex) {
            Logger.getLogger(Imagem.class.getName())
                    .log(Level.SEVERE, null, ex);
        }
        return null;
    }
    
    private void exibirImagem(){
        img = abrirImagem();
        
        ImageIcon i = new ImageIcon(img);
        jLabel1.setIcon(i);
        jLabel1.setText("");
        
        int pixels = img.getWidth() * img.getHeight();
        jLabel2.setText("Quantidade de pixels: " + pixels);
    }
    
    public void exibirRgb(){
        Color cor = new Color(img.getRGB(0, 0));
        System.out.println("Vermelho: " + cor.getRed() + 
                " Verde: " + cor.getGreen() + " Azul: " + cor.getBlue());
    }
    
    public void pintarPixel(){
        Color cor = new Color(255, 255, 255);
        img.setRGB(0, 0, cor.getRGB());
        JOptionPane.showMessageDialog(rootPane,
                "Primeiro pixel da imagem pintado com sucesso!");
    }
