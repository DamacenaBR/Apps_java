    private void grayscaleRedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grayscaleRedActionPerformed
        for(int x = 0; x < img.getWidth(); x++){
            for(int y = 0; y < img.getHeight(); y++){
                int red = new Color(img.getRGB(x, y)).getRed();
                Color escalaRed = new Color(red, red, red);
                img.setRGB(x, y, escalaRed.getRGB());
            }
        }
        jLabel1.setIcon(new ImageIcon(img));
    }//GEN-LAST:event_grayscaleRedActionPerformed

